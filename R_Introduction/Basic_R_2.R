# Logic and Math Operations

# Math Operations

a = 5 + 5
print(a)

a = 10 - 8
print(a)

a = 9/3
print(a)

a = 4*2
print(a)

a = 4**3
print(a)

a = 4^2
print(a)

# Logic Operations

a = 5 == 6
print(a)

a = 5 != 6
print(a)

a = 5 > 6
print(a)

a = 5 < 6
print(a)

a = 5 <= 6
print(a)

a = 5 >= 5
print(a)

a = 6 == 6 & 7 == 8
print(a)

a = 6 == 6 | 7 == 8
print(a)

a = !(6 == 6)
print(a)